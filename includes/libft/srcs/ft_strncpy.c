#include "../includes/libft.h"

void	ft_strncpy(char *str1, char *str2, int nb)
{
	int	i;

	i = 0;
	while (i < nb && str2[i])
	{
		str1[i] = str2[i];
		i++;
	}
	str1[i] = '\0';
	return ;
}
